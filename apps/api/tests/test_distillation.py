def test_distill_writes_page_and_sets_dna_purpose(client, tmp_path) -> None:
    settings.knowledge_root = tmp_path
    project_id = _project(client)
    repo_id = _register_repo(
        client, project_id, name="Free LLM API Resources", local_path="free-llm", readme=_FREE_LLM_README
    )

    # Seed a DNA row so distillation can enrich technologies + stamp purpose.
    session = _same_file_session(tmp_path)
    try:
        session.add(
            RepositoryDNA(
                repository_id=repo_id,
                package_managers=["pip"],
                scan_summary={"summary": {"language_classes": {"Python": "source", "Markdown": "docs"}}},
            )
        )
        session.commit()
    finally:
        session.close()

    resp = client.post(f"/repositories/{repo_id}/distill")
    assert resp.status_code == 200, resp.text
    page = resp.json()
    assert page["page_type"] == "repository"
    assert page["validation_state"] == "derived"
    assert page["vault_path"] == "wiki/repositories/free-llm-api-resources.md"
    assert page["title"] == "Free LLM API resources"
    ref_types = {ref["type"] for ref in page["source_refs"]}
    assert "repository" in ref_types and "vault_file" in ref_types

    written = tmp_path / page["vault_path"]
    assert written.is_file()
    text = written.read_text(encoding="utf-8")
    assert "# Free LLM API resources" in text
    assert "This lists various services" in text
    assert "pip" in text  # DNA package manager surfaced as a technology
    assert "Python" in text  # DNA source language surfaced

    # DNA.purpose stamped with the distilled summary.
    session = _same_file_session(tmp_path)
    try:
        dna = session.query(RepositoryDNA).filter(RepositoryDNA.repository_id == repo_id).one()
        assert dna.purpose.startswith("This lists various services")
    finally:
        session.close()

    # Projected page is queryable and typed.
    pages = client.get("/knowledge/pages", params={"page_type": "repository"}).json()
    assert page["id"] in [p["id"] for p in pages]


def test_distill_is_idempotent(client, tmp_path) -> None:
    settings.knowledge_root = tmp_path
    project_id = _project(client)
    repo_id = _register_repo(client, project_id, name="Widget Toolkit", local_path="widget", readme=_RICH_README)

    first = client.post(f"/repositories/{repo_id}/distill")
    second = client.post(f"/repositories/{repo_id}/distill")
    assert first.status_code == 200 and second.status_code == 200, second.text
    assert first.json()["vault_path"] == second.json()["vault_path"]
    assert first.json()["id"] == second.json()["id"]

    # One file, one page.
    matches = list((tmp_path / "wiki" / "repositories").glob("*.md"))
    assert len(matches) == 1
    session = _same_file_session(tmp_path)
    try:
        assert (
            session.query(KnowledgePage)
            .filter(KnowledgePage.vault_path == "wiki/repositories/widget-toolkit.md")
            .count()
            == 1
        )
    finally:
        session.close()


