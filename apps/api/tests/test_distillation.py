    capped = select_source_files(repository, cap_files=1)
    assert len(capped) == 1
    assert capped[0]["path"] == "app.py"

    # A running byte cap is honoured (0 budget → nothing selected).
    assert select_source_files(repository, cap_bytes=0) == []


